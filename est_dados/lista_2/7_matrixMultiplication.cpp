#include <iostream>
#define MAX 100
using namespace std;

// O(n^3); Adimite matrizes nxn
void multiplica(int n, int A[MAX][MAX], int B[MAX][MAX], int C[MAX][MAX]) {
    int sum;
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < n; j++) {
            sum = 0;
            for(int k = 0; k < n; k++) {
                sum += A[i][k] * B[k][j];
            }
            C[i][j] = sum;
        }
    }
}

